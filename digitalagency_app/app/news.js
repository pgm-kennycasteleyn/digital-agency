function convertNews (nieuws) {
  let tempStr = '';
  nieuws.forEach(function (nw, index) {
    const authors = convertAuthorsToString(nw.authors);
    const createDate = dateCreatedAndModified();
    //const comments = convertCommentsToString(nw.comments);
    tempStr += `
    ========================================================================
    |                           NEWS                                       |
    ========================================================================
    Project ${index + 1}: ${nw.title}
    ========================================================================
    Synopsis:\t ${nw.synopsis}\n
    Body:\t ${nw.body}\n
    Thumbnail URL:${nw.thumbnail}\n
    ${createDate}
    -------------------------------------------------------------------------
    Authors
    -------------------------------------------------------------------------
    ${authors}
    -------------------------------------------------------------------------
    Comments
    -------------------------------------------------------------------------
    
    -------------------------------------------------------------------------
    =========================================================================`;
  });
  return tempStr;
};


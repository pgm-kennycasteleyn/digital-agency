function convertAuthorsToString (authors) {
  let tempStr = '';
  authors.forEach(function (aut, index) {
    tempStr += `Author ${index + 1}: ${aut.firstName} ${aut.lastName}\n    `;
  });
  return tempStr;
};

function dateCreatedAndModified () {
  const dateCreated = new Date(1603272216284);
  const dateModified = new Date(1603272216284);
  return `Created At: ${dateCreated}\n
    Modified at: ${dateModified}`;
};

